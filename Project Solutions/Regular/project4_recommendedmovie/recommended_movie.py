rating=input("Enter a movie rating from 1 to 10")
print("Movie that is a ",rating,"out of 10")
if(rating==10):
    print("Spiderman: Into the Spiderverse")
elif(rating>=8):
    print("Encanto")
elif(rating>=6):
    print("Lion King 1.5")
elif(rating>=4):
    print("Wreck it Ralph 2-Ralph Breaks the Internet")
elif(rating>=2):
    print("The Lion King (2019)")
else:
    print("Son of Aladdin")